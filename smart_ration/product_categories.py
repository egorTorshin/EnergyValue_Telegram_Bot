def get_category_keyboard() -> List[List[str]]:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º–∏ –ø—Ä–æ–¥—É–∫—Ç–æ–≤"""
    categories = list(PRODUCT_CATEGORIES.keys())
    keyboard = []
    
    # –†–∞–∑–º–µ—â–∞–µ–º –ø–æ 2 –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –≤ —Ä—è–¥
    for i in range(0, len(categories), 2):
        row = [categories[i]]
        if i + 1 < len(categories):
            row.append(categories[i + 1])
        keyboard.append(row)
    
    return keyboard

